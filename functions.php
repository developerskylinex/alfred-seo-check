function setServiceValue( $service, $key, $value ) {
	$data = $_SERVER[ 'HOME' ] . '/Library/Application Support/Alfred 2/Workflow Data/com.spr.seo';
	$config = json_decode( file_get_contents( "$data/config.json" ), TRUE );
	if ( $service == 'Google_Pagerank' )
		$service = 'Google Pagerank';
	$credentials = array( 'accessID', 'token', 'secretKey' );
	if ( ! in_array( $key, $credentials ) )
		$config[ 'services' ][ $service ][ $key ] = $value;
	else
		$config[ 'services' ][ $service ][ 'credentials' ][ $key ] = $value;
	file_put_contents( "$data/config.json", json_encode( $config ) );
}

function getServiceValue( $service, $key ) {
	$data = $_SERVER[ 'HOME' ] . '/Library/Application Support/Alfred 2/Workflow Data/com.spr.seo';
	$config = json_decode( file_get_contents( "$data/config.json" ), TRUE );
	if ( isset( $config[ 'services' ][ $service ][ $key ] ) )
		return $config[ 'services' ][ $service ][ $key ];
	else
		return FALSE;
}

function addURL( $url ) {
	$data = $_SERVER[ 'HOME' ] . '/Library/Application Support/Alfred 2/Workflow Data/com.spr.seo';
	$config = json_decode( file_get_contents( "$data/config.json" ), TRUE );
	if ( isset( $config[ 'urls' ] ) ) {
		if ( ! in_array( $url, $config[ 'urls' ] ) ) {
			$config[ 'urls' ][] = $url;
		} else {
			return FALSE;
		}
	} else {
		$config[ 'urls' ][] = $url;
	}
	file_put_contents( "$data/config.json", json_encode( $config ) );
}

function removeURL( $url ) {
	$data = $_SERVER[ 'HOME' ] . '/Library/Application Support/Alfred 2/Workflow Data/com.spr.seo';
	$config = json_decode( file_get_contents( "$data/config.json" ), TRUE );
	if ( isset( $config[ 'urls' ] ) ) {
		if ( in_array( $url, $config[ 'urls' ] ) ) {
			unset( $config[ 'urls' ][ array_search( $url, $config[ 'urls' ] ) ] );
		} else {
			return FALSE;
		}
	} else {
		return FALSE;
	}
	file_put_contents( "$data/config.json", json_encode( $config ) );	
}

/**
 * Encrypts a string with a salted ssl base64 encoding
 * @param  string $string a string, here, we use a password string
 * @return string         an encoded password string
 */
function encrypt_string( $string ) {
	
	$string  = $this->salt . $string . $salt;
	$cmd = 'out=$(echo "' . $string . '" | openssl base64 -e); echo "${out}"';
	return exec( $cmd );
}

/**
 * Decrypts a salted ssl base64 encoding string
 * @param  string $string encrypted password string
 * @return string         decrypted password string
 */
function decrypt_string( $string ) {
	
	$cmd   = 'out=$(echo "' . $string . '" | openssl base64 -d); echo "${out}"';
	return str_replace( $this->salt, '', exec( $cmd ) );
	
}


function checkDomain( $domain ) {

	if ( strpos( $domain, 'http://' ) === 0 ) {
		$domain = str_replace( 'http://', '', $domain );
	}
	if ( strpos( $domain, 'https://' ) === 0 ) {
		$domain = str_replace( 'https://', '', $domain );
	}

	$domain = explode( '.', $domain );
	if ( count( $domain ) > 1 ) {
		if ( ! checkTLD( $domain[ count( $domain ) - 1 ] ) ) {
			return FALSE;
		} else {
			return TRUE;
		}
	}
	return FALSE;
}



